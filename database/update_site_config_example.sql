-- ============================================
-- 更新網站設定範例
-- ============================================
-- 此檔案提供各種更新 site_config 的 SQL 範例

USE event_wall;

-- ============================================
-- 範例 1: 更新所有基本設定 (婚禮範例)
-- ============================================
UPDATE site_config
SET
    guest_name_a = '王小明',
    guest_name_b = '李小華',
    event_date = '2025-06-15',
    site_title = '小明❤️小華的婚禮',
    welcome_message = '感謝您參加我們的婚禮，請留下您的祝福與美好回憶！'
WHERE id = 1;

-- ============================================
-- 範例 2: 更新公司尾牙活動設定
-- ============================================
UPDATE site_config
SET
    guest_name_a = 'ABC科技',
    guest_name_b = '2025尾牙',
    event_date = '2025-01-20',
    site_title = 'ABC科技2025尾牙',
    welcome_message = '歡迎來到ABC科技2025年度尾牙晚會！請盡情拍照、上傳與互動！'
WHERE id = 1;

-- ============================================
-- 範例 3: 更新生日派對設定
-- ============================================
UPDATE site_config
SET
    guest_name_a = '壽星',
    guest_name_b = '張小美',
    event_date = '2025-03-10',
    site_title = '小美的生日派對',
    welcome_message = '歡慶小美的生日！請留下你的祝福和歡樂時刻～'
WHERE id = 1;

-- ============================================
-- 範例 4: 只更新單一欄位 (網站標題)
-- ============================================
UPDATE site_config
SET site_title = '我們的特別日子'
WHERE id = 1;

-- ============================================
-- 範例 5: 只更新單一欄位 (歡迎訊息)
-- ============================================
UPDATE site_config
SET welcome_message = '歡迎來到我們的活動互動牆，請留下您的祝福！'
WHERE id = 1;

-- ============================================
-- 範例 6: 只更新活動日期
-- ============================================
UPDATE site_config
SET event_date = '2025-12-31'
WHERE id = 1;

-- ============================================
-- 範例 7: 只更新嘉賓名稱
-- ============================================
UPDATE site_config
SET
    guest_name_a = '新郎',
    guest_name_b = '新娘'
WHERE id = 1;

-- ============================================
-- 查詢目前設定 (執行更新前後可用於檢查)
-- ============================================
SELECT * FROM site_config WHERE id = 1;

-- ============================================
-- 查詢目前設定 (格式化顯示)
-- ============================================
SELECT
    CONCAT(guest_name_a, ' ❤️ ', guest_name_b) AS '嘉賓名稱',
    event_date AS '活動日期',
    site_title AS '網站標題',
    welcome_message AS '歡迎訊息',
    updated_at AS '最後更新時間'
FROM site_config
WHERE id = 1;
